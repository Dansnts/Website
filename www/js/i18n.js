// Apply translations
function applyTranslations(lang, pageName) {
  // Common translations
  Object.entries(commonTranslations[lang]).forEach(([key, value]) => {
    document.querySelectorAll(`[data-i18n="${key}"]`).forEach(el => {
      el.textContent = value;
    });
  });

  // Page-specific translations, on index (single page), apply all page sets
  const pageSets = pageName === 'index'
    ? ['index', 'projects', 'cv']
    : [pageName];

  pageSets.forEach(page => {
    if (pageTranslations[page] && pageTranslations[page][lang]) {
      Object.entries(pageTranslations[page][lang]).forEach(([key, value]) => {
        document.querySelectorAll(`[data-i18n="${key}"]`).forEach(el => {
          if (value.includes('<')) {
            el.innerHTML = value;
          } else {
            el.textContent = value;
          }
        });
      });
    }
  });

  // CV-specific translations
  if (pageName === 'cv' || pageName === 'index') {
    // Jobs
    const sysadminTitle = document.querySelector('[data-i18n="job-sysadmin"]');
    if (sysadminTitle) sysadminTitle.textContent = jobTranslations[lang].sysadmin.title;

    const sysadminTasks = document.querySelectorAll('[data-i18n="sysadmin-task"]');
    sysadminTasks.forEach((el, i) => {
      if (jobTranslations[lang].sysadmin.tasks[i]) {
        el.textContent = jobTranslations[lang].sysadmin.tasks[i];
      }
    });

    const traineeTitle = document.querySelector('[data-i18n="job-trainee"]');
    if (traineeTitle) traineeTitle.textContent = jobTranslations[lang].trainee.title;

    const traineeTasks = document.querySelectorAll('[data-i18n="trainee-task"]');
    traineeTasks.forEach((el, i) => {
      if (jobTranslations[lang].trainee.tasks[i]) {
        el.textContent = jobTranslations[lang].trainee.tasks[i];
      }
    });

    // Education
    const bachelor = document.querySelector('[data-i18n="degree-bachelor"]');
    if (bachelor) bachelor.textContent = educationTranslations[lang].bachelor;

    const maturity = document.querySelector('[data-i18n="degree-maturity"]');
    if (maturity) maturity.textContent = educationTranslations[lang].maturity;

    const maturityDetail = document.querySelector('[data-i18n="degree-maturity-detail"]');
    if (maturityDetail) {
      const link = maturityDetail.querySelector('a');
      if (link) {
        maturityDetail.innerHTML = '';
        maturityDetail.appendChild(link);
        maturityDetail.append(lang === 'fr' ? ' - Architecture et sciences de la vie' : ' - Architecture and Life Sciences');
      } else {
        maturityDetail.textContent = educationTranslations[lang].maturityDetail;
      }
    }

    const cfc = document.querySelector('[data-i18n="degree-cfc"]');
    if (cfc) cfc.textContent = educationTranslations[lang].cfc;

    // TB
    const tbTitle = document.querySelector('[data-i18n="tb-title"]');
    if (tbTitle) tbTitle.innerHTML = educationTranslations[lang].tbTitle;

    const tbDesc = document.querySelector('[data-i18n="tb-desc"]');
    if (tbDesc) tbDesc.textContent = educationTranslations[lang].tbDesc;

    // HEIG-VD DTS
    const heigCrunchtime = document.querySelector('[data-i18n="heig-crunchtime"]');
    if (heigCrunchtime) heigCrunchtime.textContent = educationTranslations[lang].heigCrunchtime;

    const heigDts = document.querySelector('[data-i18n="heig-dts"]');
    if (heigDts) heigDts.textContent = educationTranslations[lang].heigDts;

    // ETML
    const etmlPrize = document.querySelector('[data-i18n="etml-prize"]');
    if (etmlPrize) etmlPrize.textContent = educationTranslations[lang].etmlPrize;

    const etmlVoip = document.querySelector('[data-i18n="etml-voip"]');
    if (etmlVoip) etmlVoip.textContent = educationTranslations[lang].etmlVoip;

    const etmlAzure = document.querySelector('[data-i18n="etml-azure"]');
    if (etmlAzure) etmlAzure.textContent = educationTranslations[lang].etmlAzure;

    // Pré-apprentissage
    const preapp = document.querySelector('[data-i18n="degree-preapp"]');
    if (preapp) preapp.textContent = educationTranslations[lang].preapp;

    const preappPicamera = document.querySelector('[data-i18n="preapp-picamera"]');
    if (preappPicamera) preappPicamera.textContent = educationTranslations[lang].preappPicamera;

    const preappPrize = document.querySelector('[data-i18n="preapp-prize"]');
    if (preappPrize) preappPrize.textContent = educationTranslations[lang].preappPrize;
  }
}

// Detect current page
function getCurrentPage() {
  const path = window.location.pathname;
  // Check for project detail pages first
  const projectMatch = path.match(/\/projects\/([^.\/]+)\.html/);
  if (projectMatch) return 'project-' + projectMatch[1];
  // Everything else is the single-page index
  return 'index';
}

// Listen for language changes
window.addEventListener('langChange', (e) => {
  applyTranslations(e.detail, getCurrentPage());
});

